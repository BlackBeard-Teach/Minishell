#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int i;
	int j;

	i = 0;
	j = 0;
	if (s != '\0' && f != '\0')
	{
		i = ft_strlen(s);
		while (j < i)
		{
			(*f)(s);
			s++;
			j++;
		}
	}
}
